
// 1
const arrowDownOne = document.querySelector('.arrow-down-one');
const arrowUpOne = document.querySelector('.arrow-up-one');
const paragraphHiddenOne = document.querySelector('.paragraph-hidden-one');
const titleVisibleOne = document.querySelector('.title-visible-one');
const paraSvgCont = document.querySelector('.para-svg-cont');


arrowDownOne.onclick = function() {
  arrowDownOne.style.display = 'none';
  arrowUpOne.style.display = 'block';
  paragraphHiddenOne.style.display = 'block';
  titleVisibleOne.style.borderBottom = '3px solid #a78847';
}

arrowUpOne.onclick = function () {
  arrowUpOne.style.display = 'none';
  arrowDownOne.style.display = 'block';
  paragraphHiddenOne.style.display = 'none';
  titleVisibleOne.style.borderBottom = 'none';
}

